package org.javaInterviewQuestions;

public class WhileDoWhile {

	public static void main() {
		int i=1;
		while(i>=10) {
			System.out.println("Java");
			i++;
			
		}
	}
	public static void main1() {
		int i=10;
		do {
			System.out.println("Selenium");
			i++;
		}while(i<=20);
	}
	public static void main(String[] args) {
		main();
		main1();
	}
}
